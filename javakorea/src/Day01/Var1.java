package Day01;

public class Var1 {
	public static void main(String[] args) {
		// 프로그래밍 언어: 개발자가 무언가를 코딩해서 컴퓨터가 결과를 출력
		// 변수: 저장공간 (무언가 값을 저장해놓았다가 키워드로 사용)
		String 이름 = "홍길동";	// "홍길동"이라는 값을 "이름"이라는 공간에 저장
		
		System.out.println(이름+"1");
		System.out.println("홍길동2");
		System.out.println("홍길동3");
		System.out.println("홍길동4");
		System.out.println("홍길동5");
		System.out.println("홍길동6");
	}
}
