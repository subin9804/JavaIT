package Day01;

public class Var2 {
	public static void main(String[] args) {
		// 세미콜론은 문장이 끝났음을 의미함
		String 변수1 = "낚시" ;
			
		System.out.println("Hello "+변수1+"!");
		
		System.out.println(변수1+" is simple");
		System.out.print("안녕하세요");
		System.out.print("반갑습니다"); // ln을 지워주면 줄바꿈을 하지 않는다.
		System.out.println();
		System.out.println(변수1+"를 공부합니다");
	}
}
